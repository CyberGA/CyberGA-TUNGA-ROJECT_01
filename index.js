// addition
function add(arg1, arg2) {
  return arg1 + arg2;
}
const result = add(8, 8);
console.log(result);

//subtraction
function subtraction(arg1, arg2) {
  return arg1 - arg2;
}
const result1 = subtraction(16, 8);
console.log(result1);

//multiplication
function multiplication(arg1, arg2) {
  return arg1 * arg2;
}
const result2 = multiplication(4, 8);
console.log(result2);